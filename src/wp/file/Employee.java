package wp.file;

public class Employee {
	private int eno;
	private String ename;
	private double sal;
	private String desg;
	private String dept;

	public Employee(int eno, String ename, double sal, String desg, String dept) {
		super();
		this.eno = eno;
		this.ename = ename;
		this.sal = sal;
		this.desg = desg;
		this.dept = dept;
	}

}
